const INJECTDLL_HOOKCTX_32: usize = 0x0190;
const INJECTDLL_HOOKCTX_64: usize = 0x0250;

#[repr(C, packed)]
struct InjectHookCtx {
    ntdll_base: u64,
    thunk_addr: u64,
    dll_path: [u16; MAX_PATH],
    func_name: [u8; MAX_PATH],
}

impl InjectHookCtx {
    pub unsafe fn to_bytes(&self) -> &[u8] {
        let p: *const Self = self;
        let p = p as *const u8;
        std::slice::from_raw_parts(p, std::mem::size_of::<Self>())
    }
}

pub fn build_injected_code(
    is64bit: bool,
    ntdll_base: usize,
    thunk_addr: usize,
    dll_path: &str,
    func_name: &str,
) -> Vec<u8> {
    let (mut code, ctx_ofs) = match is64bit {
        true => (INJECTDLL_SC_64.to_vec(), INJECTDLL_HOOKCTX_64),
        false => (INJECTDLL_SC_32.to_vec(), INJECTDLL_HOOKCTX_32),
    };

    let mut ctx = InjectHookCtx {
        ntdll_base: ntdll_base as u64,
        thunk_addr: thunk_addr as u64,
        dll_path: [0u16; MAX_PATH],
        func_name: [0u8; MAX_PATH],
    };

    let wdll_path: Vec<u16> = dll_path.encode_utf16().collect();
    unsafe {
        std::ptr::copy(
            wdll_path.as_ptr(),
            std::ptr::addr_of_mut!(ctx.dll_path) as *mut u16,
            wdll_path.len(),
        );
    }

    ctx.func_name[..func_name.as_bytes().len()].clone_from_slice(func_name.as_bytes());
    code[ctx_ofs..ctx_ofs + mem::size_of::<InjectHookCtx>()]
        .clone_from_slice(unsafe { ctx.to_bytes() });

    code
}
